// Little Complex Example

console.log( "2" + "3" ); // 23
console.log( "2" - "3" ); // -1
console.log( "2" * "3" ); // 6
console.log( "2" / "3" ); // 0.6666666666666666
console.log( "2" % "3" ); // 2
console.log( "2" ** "3" ); // 8

console.log( "2" + 2 + 7 ); // 227
console.log( "2" - 2 - 7 ); // -5
console.log( "2" * 2 * 7 ); // 56
console.log( "2" / 2 / 7 ); // 0.14285714285714285
console.log( "2" % 2 % 7 ); // 2
console.log( "2" ** 2 ** 7 ); // 2097152

